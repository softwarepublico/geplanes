package br.com.linkcom.sgm.util;

import java.io.IOException;

import javax.servlet.FilterChain;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import br.com.linkcom.neo.core.web.NeoFilter;

/**
 * @author Pedro Gonçalves
 */
public class RequestFilter extends NeoFilter {

	@Override
	protected void doFilterInternal(HttpServletRequest req, HttpServletResponse res, FilterChain chain) throws ServletException, IOException {
		
		for (String[] n : Nomes.values()) {
			req.setAttribute(n[0], n[1]);
		}	
		
		super.doFilterInternal(req, res, chain);
}

	
	
}
